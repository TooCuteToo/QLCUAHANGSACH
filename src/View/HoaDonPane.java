    private void them_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_them_btnActionPerformed
        try {
            // TODO add your handling code here:
            String ma_hoa_don = ma_hd_txt.getText();
            String ma_sach = ma_sach_txt.getText();
            String ma_khach = ma_khach_txt.getText();
            float thanh_tien = Float.parseFloat(thanh_tien_txt.getText());

            HoaDonPoJo hoa_don = new HoaDonPoJo(ma_hoa_don, ma_sach, ma_khach, thanh_tien);
            hoa_don_DAO.addHoaDon(hoa_don);

            hoa_don_model.addElement(hoa_don);
            resetTxtHoaDon();

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không hơp lệ");
        }
    }//GEN-LAST:event_them_btnActionPerformed

    private void cap_nhat_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cap_nhat_btnActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            String maHD = hoa_don_model.getByIndex(jTable1.getSelectedRow()).getMaHD();
            String ma_hoa_don = ma_hd_txt.getText();
            
            String ma_sach = ma_sach_txt.getText();
            String ma_khach = ma_khach_txt.getText();
            
            float thanh_tien = Float.parseFloat(ma_khach_txt.getText());

            int index = jTable1.getSelectedRow();
            HoaDonPoJo hoa_don = hoa_don_model.getByIndex(index);

            HoaDonPoJo hoa_don_update = new HoaDonPoJo(ma_hoa_don, ma_sach, ma_khach, thanh_tien);

            hoa_don_DAO.updateHoaDon(hoa_don_update, maHD);
            initHoaDonTable();
            resetTxtHoaDon();
            cap_nhat_btn.setEnabled(false);
            xoa_btn.setEnabled(false);

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không hơp lệ");
        }
    }//GEN-LAST:event_cap_nhat_btnActionPerformed

    private void xoa_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoa_btnActionPerformed
        try {
            // TODO add your handling code here:
            int index = jTable1.getSelectedRow();
            HoaDonPoJo hoa_don = hoa_don_model.getByIndex(index);

            hoa_don_DAO.removeHoaDon(hoa_don);
            hoa_don_model.removeElement(index);

            cap_nhat_btn.setEnabled(false);
            xoa_btn.setEnabled(false);

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không hơp lệ");
        }
    }//GEN-LAST:event_xoa_btnActionPerformed

    private void search_btn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_btn2ActionPerformed
        try {
            // TODO add your handling code here:
            String ma_hoa_don = ma_hd_txt.getText();
            String ma_sach = ma_sach_txt.getText();
            
            String ma_khach = ma_khach_txt.getText();
            float thanh_tien = -1;

            if (!ma_khach_txt.getText().isEmpty()) {
                thanh_tien = Float.parseFloat(ma_khach_txt.getText());
            }

            HoaDonPoJo hoa_don = new HoaDonPoJo(ma_hoa_don, ma_sach, ma_khach, thanh_tien);
            hoa_don_model.getBySearch(hoa_don);
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không hơp lệ");
        }
    }//GEN-LAST:event_search_btn2ActionPerformed

