    private void handleSelected() {
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override

            // ! -> selected 
            public void valueChanged(ListSelectionEvent lse) {
                try {
                    if (!lse.getValueIsAdjusting()) {
                        HoaDonPoJo hoa_don = (HoaDonPoJo) hoa_don_model.getByIndex(jTable1.getSelectedRow());

                        ma_hd_txt.setText(hoa_don.getMaHD());
                        ma_sach_txt.setText(hoa_don.getMaSH());
                        
                        ma_khach_txt.setText(hoa_don.getMaKH());
                        thanh_tien_txt.setText(String.valueOf(hoa_don.getThanhTien()));

                        xoa_btn.setEnabled(true);
                        cap_nhat_btn.setEnabled(true);
                    }
                } catch (Exception e) {
                    System.out.print("hello");
                }
                //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }
        });

    }

