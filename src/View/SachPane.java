    private void them_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_them_btnActionPerformed
        try {
            // TODO add your handling code here:
            String ma_sh = ma_sh_txt.getText();
            String ten_sh = ten_sh_txt.getText();

            String ma_nxb = ma_nxb_txt.getText();
            String ma_tg = ma_tg_txt.getText();

            String tinh_trang = tinh_trang_txt.getText();
            int so_luong = Integer.parseInt(so_luong_txt.getText());

            SachPoJo sach = new SachPoJo(ma_sh, ten_sh, ma_nxb, ma_tg, tinh_trang, so_luong);
            sach_DAO.addSach(sach);

            sach_model.addElement(sach);
            resetTxtSach();

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không hơp lệ");
        }
    }//GEN-LAST:event_them_btnActionPerformed

    private void cap_nhat_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cap_nhat_btnActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            String maSH = sach_model.getByIndex(jTable1.getSelectedRow()).getMaSH();
            String ma_sh = ma_sh_txt.getText();
            String ten_sh = ten_sh_txt.getText();

            String ma_nxb = ma_nxb_txt.getText();
            String ma_tg = ma_tg_txt.getText();

            String tinh_trang = tinh_trang_txt.getText();
            int so_luong = Integer.parseInt(so_luong_txt.getText());

            int index = jTable1.getSelectedRow();
            SachPoJo sach = sach_model.getByIndex(index);

            SachPoJo sach_cap_nhat = new SachPoJo(ma_sh, ten_sh, ma_nxb, ma_tg, tinh_trang, so_luong);

            sach_DAO.updateSach(sach_cap_nhat, maSH);
            initSachTable();
            resetTxtSach();

            cap_nhat_btn.setEnabled(false);
            xoa_btn.setEnabled(false);
            resetTxtSach();

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không hơp lệ");
        }
    }//GEN-LAST:event_cap_nhat_btnActionPerformed

    private void xoa_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoa_btnActionPerformed
        try {
            // TODO add your handling code here:
            int index = jTable1.getSelectedRow();
            SachPoJo sach = sach_model.getByIndex(index);

            sach_DAO.removeSach(sach);
            sach_model.removeElement(index);

            cap_nhat_btn.setEnabled(false);
            xoa_btn.setEnabled(false);
            resetTxtSach();

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không hơp lệ");
        }
    }//GEN-LAST:event_xoa_btnActionPerformed

