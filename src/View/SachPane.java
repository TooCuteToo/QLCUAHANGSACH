    public void initSachTable() throws ClassNotFoundException, SQLException {
        sach_model = new SachModel();
        jTable1.setModel(sach_model);
        handleSelected();
    }

    private void handleSelected() {
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override

            // ! -> selected 
            public void valueChanged(ListSelectionEvent lse) {
                try {
                    if (!lse.getValueIsAdjusting()) {
                        SachPoJo sach = (SachPoJo) sach_model.getByIndex(jTable1.getSelectedRow());

                        ma_sh_txt.setText(sach.getMaSH());
                        ten_sh_txt.setText(sach.getTenSH());

                        ma_nxb_txt.setText(sach.getMaNXB());
                        ma_tg_txt.setText(sach.getMaTG());

                        tinh_trang_txt.setText(sach.getTinhTrang());
                        so_luong_txt.setText(String.valueOf(sach.getSoLuong()));

                        xoa_btn.setEnabled(true);
                        cap_nhat_btn.setEnabled(true);
                    }
                } catch (Exception e) {
                    System.out.print("hello");
                }
                //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }
        });

    }

